from typing import Any, Dict, List

import httpx

import opik.guardrails.schemas as schemas
from opik.rest_api import core as rest_api_core


class GuardrailsApiClient:  # TODO: in future this class can be autogenerated with Fern
    def __init__(
        self,
        httpx_client: httpx.Client,
        host_url: str,
    ) -> None:
        self._api_url = host_url.rstrip("/")
        self._httpx_client = httpx_client

    def __del__(self) -> None:
        self._httpx_client.close()

    def validate(
        self, text: str, validations: List[Dict[str, Any]]
    ) -> schemas.ValidationResponse:
        payload = {"text": text, "validations": validations}

        response = self._httpx_client.post(
            f"{self._api_url}/api/v1/guardrails/validations", json=payload
        )
        if response.status_code != 200:
            raise rest_api_core.ApiError(
                status_code=response.status_code, body=response.json()
            )

        return schemas.ValidationResponse(**response.json())

    def train_custom(
        self,
        name: str,
        description: str,
        examples: List[Dict[str, Any]],
        base_model: str,
        epochs: float,
        overwrite: bool = False,
    ) -> Dict[str, Any]:
        payload = {
            "name": name,
            "description": description,
            "examples": examples,
            "base_model": base_model,
            "epochs": epochs,
            "overwrite": overwrite,
        }
        response = self._httpx_client.post(
            f"{self._api_url}/api/v1/guardrails/custom/train", json=payload
        )
        if response.status_code not in (200, 202):
            raise rest_api_core.ApiError(
                status_code=response.status_code, body=response.json()
            )

        return response.json()

    def get_custom_training_status(self, name: str) -> Dict[str, Any]:
        response = self._httpx_client.get(
            f"{self._api_url}/api/v1/guardrails/custom/train/{name}"
        )
        if response.status_code != 200:
            raise rest_api_core.ApiError(
                status_code=response.status_code, body=response.json()
            )

        return response.json()
